    <h3>NOSSA HISTÓRIA</h3>

    <br>

    <p>
      O Instituto Formiga teve sua origem vinculada à ONG Formiga Verde. A
      concepção inicial do que viria a ser a instituição aconteceu em 2005,
      quando professores do bairro Santa Felícia (São Carlos, SP) identificaram
      a necessidade de oferecer suporte às crianças com mais de 12 anos de
      idade. Esta percepção era resultado do fato de que após a conclusão do
      Projeto Recreação, essas crianças enfrentavam uma lacuna preocupante em
      termos de opções saudáveis para ocupar seu tempo livre, o que as deixavam
      mais vulneráveis a influências negativas e problemas sociais.
    </p>

